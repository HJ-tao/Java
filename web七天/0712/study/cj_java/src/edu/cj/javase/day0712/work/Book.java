package edu.cj.javase.day0712.work;

public class Book {
	
	private int buyId;
	private String texNo;
	private int bookId;
	private double bookPrice;
	private double bookDiscount;
	private double truePrice;
	private int bookMount;
	private double bookMoney;
	public int getBuyId() {
		return buyId;
	}
	public void setBuyId(int buyId) {
		this.buyId = buyId;
	}
	public String getTexNo() {
		return texNo;
	}
	public void setTexNo(String texNo) {
		this.texNo = texNo;
	}
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public double getBookPrice() {
		return bookPrice;
	}
	public void setBookPrice(double bookPrice) {
		this.bookPrice = bookPrice;
	}
	public double getBookDiscount() {
		return bookDiscount;
	}
	public void setBookDiscount(double bookDiscount) {
		this.bookDiscount = bookDiscount;
	}
	public double getTruePrice() {
		return truePrice;
	}
	public void setTruePrice(double truePrice) {
		this.truePrice = truePrice;
	}
	public int getBookMount() {
		return bookMount;
	}
	public void setBookMount(int bookMount) {
		this.bookMount = bookMount;
	}
	public double getBookMoney() {
		return bookMoney;
	}
	public void setBookMoney(double bookMoney) {
		this.bookMoney = bookMoney;
	}
	
	

}
